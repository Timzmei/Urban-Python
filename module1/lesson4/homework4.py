my_string = input('Введите произволный текст: ')

print(f'количество символов введённого текста: {len(my_string)}')

print(f'строка my_string в верхнем  регистре: {my_string.upper()}')

print(f'строка my_string в нижнем регистре: {my_string.lower()}')

print(f'из строки my_string удалены все пробелы: {my_string.replace(" ", "")}')

print(f'первый символ строки: {my_string[0]}')

print(f'последний символ строки: {my_string[-1]}')